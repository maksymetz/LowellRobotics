<div class="titlebanner">
<h2>Team Update:  Arm Dexterity</h2>
</div>
<span style="font-weight:bold;">Jan. 22 2011</span>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;Some major elements of our robot are taking shape, and our robot is finally starting to resemble an actual, well, robot! We now have an arm; 
although there is not yet a motor attached, we have br/ought the robot out into the the school hallway along with the scoring rack to test it out, simulating how 
it will move within the space restrictions. While one student guides the robot, one or two others operate the arm and the gripper prototype, picking up tubes manually 
and hanging them on our PVC contraption. This will help us to choose to tilt the gripper or not.
<br/>&nbsp;&nbsp;&nbsp;&nbsp;By the way, we decided to flip over our frame over so that the rock was minimized. Although the robot still tips ever so slightly from front 
to back, it's not quite as dramatic. It was also decided that we would connect the omni wheels to the center drive wheels, creating an all wheel drive and making it 
less likely that we will get stuck. The control board is also beginning to come together. We're on a roll!
</p>

<div class="titlebanner">
<h2>Team Update:  Drive Train</h2>
</div>
<span style="font-weight:bold;">Jan. 15 2011</span>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;So far, not much has been done on the actual robot as we are still testing our ideas and checking our calculations when it comes to torque and such. 
However, we've takent eh frame from last year's robot and added six wheels; two traction wheels in the center on each side with four omniwheels (two on the front and 
two on the back.) One way, the robot has some rock to it, while the other way the rock is minor and we're experimenting with which way to flip it. Having some tip to 
robot would ensure that we'd always have the traction wheels on the ground, thus preventing the robot from being stuck on a popped tube or something similar.
<br/>&nbsp;&nbsp;&nbsp;&nbsp;Though we do not have much of the robot built, we've already completed the scoring rack and the minibot pole for when it comes time to test 
our robot. For now, we'll just keep plugging away at our design!
</p>

<div class="titlebanner">
<h2>Team Update: Kickoff</h2>
</div>
<span style="font-weight:bold;">Jan. 8 2011</span>
<p>
&nbsp;&nbsp;&nbsp;&nbsp;Wow, do we have an exciting task ahead of us! Seeing as this is only our second year of competition, we were really looking forward to learning this 
year's competition, we were really looking forward to learning what this year's game would be like. Now that we've attended the kick-off, our gears are already spinning 
with ideas.
<br/>&nbsp;&nbsp;&nbsp;&nbsp;Our initial thought, in order to keep our robot as simple as possible, was to reuse the frame concept from last year. That way we'd have the 
same dimensions and wouldn't have to make new bumpers. We're determined to be more efficient than we were last year when we weren't quite sure how to go about doing 
everything. We also came up with the idea of using 6 wheel for reasons of traction and turn precision.
<br/>&nbsp;&nbsp;&nbsp;&nbsp;As for the arm, the decision is not yet set in stone. The current issue is determining how long each section of the arm will be so that it can 
reach to either the front or back of the robot while staying in the required 84 inches. Going along with that, we've decided that we want to build a simple gripper with 
thre or four prongs to pick up the tubes.
<br/>&nbsp;&nbsp;&nbsp;&nbsp;The task is daunting, yet we can't wait for the challenge!
</p>

