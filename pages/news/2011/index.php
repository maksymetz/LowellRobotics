<div class="titlebanner">
<h2>Team Update:  We Have a Robot</h2>
</div>

<img src="/img/IMG_5021" class="newspic" alt="Page Under Construction!" />

<span style="font-weight:bold;">Feb. 9 2011</span>
<p>
	&nbsp;&nbsp;&nbsp;&nbsp;With only a few weeks left of the build season; our efficiency compared to last year is already evident. Not only do we already have a functioning robot, which 
is a feat in itself, but we have driven it on multiple occasions, and the arm which will be used to pick up tubes is nearly complete. In fact, a lot of what we've been working on 
lately has been merely tweaks, touch-ups, and updates.
	<br/>&nbsp;&nbsp;&nbsp;&nbsp;One of our mottos throughout this build has been to keep everything as simple as possible, and we achieved just that in the minibot deployment system. Our set-up involves 
two drawer slides raised above the control panel by a metal platform. When a switch is lifted, the deployment tray springs out, and the wheels of the minibot snap 
around and attach to the pole on contact. They'll already be spinning at this point, allowing for a swift ascent.
	<br/>&nbsp;&nbsp;&nbsp;An addition to the robot that we're all grateful for is the new bumper system. We're using the same bumpers as were used last year, but the new attachment system 
we've come up with is much more convenient. Last year, there were bolts connected to the back of the bumpers, so when it came time to switch colors, team members had 
to reach up into the robot to unscrew the knuts, often working blind. Now, however, all we have to do is slide the bumpers on and off using modified u-channel and drop 
pins to keep them in place.
	<br/>&nbsp;&nbsp;&nbsp;&nbsp;In all, the robot is really coming together. We're just finishing the arm, which will be able to pick up tubes from both the front and back. The programmers have 
been working diligently on autonomous code, and they have basic controls coded with plans to include encoders. It's hard to believe that this robot was once nothing more 
than a box of parts. We look forward to competitions, to seeing how other teams have assembled their robots, to enjoying yet another season of all FIRST has to offer.
</p>

