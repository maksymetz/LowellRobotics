<h3>What is FRC?</h3>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The FIRST Robotics Competition (FRC) is a program of FIRST encompassing over 2,200 teams which embody more than 55,000 high-school students.  The annual game challenge
is released on the first weekend of January, at "Kickoff".  Teams then enter "Build Season", a 6-week period during which a robot must be prototyped, designed,
constructed, and programmed to participate in the competitions.</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;In 1992, the FIRST Robotics Competition was held for the first time in a high school gym in New Hampshire.  In the game, "Maize Craze",
Robots had to go around the gym and pick up corn to BETTER DESCRIPTION.  In contrast, this years game, "Logomotion", will be hosted worldwide, eventually culminating in
the World Competition at St. Louis.</p>

