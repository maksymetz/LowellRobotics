<h1>About FIRST</h1>
<h3>What is FIRST?</h3>
<img src="/pages/first/img/FIRST.png" class="newspic" alt="FIRST" />
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;FIRST (For Inspiration of Science and Technology) is an organization committed, in the words of
founder Dean Kamen, "To transform our culture by creating a world where science and technology are celebrated and where young people dream of becoming science and
technology leaders."  FIRST was founded in 1989 with this mission and has since evolved into an immense program influencing the lives of over 250,000 students worldwide!
FIRST aims to inspire these young people to lead careers in science and technology by engaging them in exciting mentor-based programs which will build science, demand
innovation, and equip them to succeed in their future endeavors by developing skills that encourage communication, teamwork, leadership, and self-confidence.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Dr. Woodie Flowers, the FIRST National Advisor, describes the excellent attitude desired of all students--high-quality work, emphasizing
the value of others, respecting individuals and the community--as "Gracious Professionalism".  FIRST inspires students to learn and compete at full-throttle, but it also
reminds them by Gracious Professionalism to treat one another with respect and generosity.  As a product of this gracious behavior, the FIRST concept of Coopertition will
be realized.  Coopertition involves learning from everybody, and paying back what you've learned to everybody.  It involves helping and cooperating with other teams even
as you compete against them.</p>
