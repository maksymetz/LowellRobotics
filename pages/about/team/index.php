<h1>Red Arrow Robotics</h1>
<p>FIRST Robotics Competition team #3234, a.k.a. Red Arrow Robotics is a school sponsored club from Lowell, Michigan.</p>
<div class="titlebanner">
<h2 id="newstitle">History</h2>
</div>
<p>In 2008, a student approached Al Eckman to request he be the head mentor for a FRC robotics team. He agreed and applied for a grant from the Lowell Community Fund.  The funds were used the first year to gain experience with smaller robots.  The following year we moved forward in the FIRST program, graduating to larger robots. </p>

<div class="titlebanner"><h2>Purpose</h2></div>
<p>To increase the understanding of science, engineering, and technology in everyday life while building self-confidence, communication, and leadership skills in local youth through robotics and community involvement.</p>

<div class="titlebanner"><h2>Vision</h2></div>
<p>To become a world class team in the FIRST program and enable our students to be the STEM leaders of their generation.</p>

<div class="titlebanner"><h2>Mission</h2></div>
<p>Develop a team that will design and build competitive robots, educate the community in STEM, and become self-sustaining through a business program and community sponsorship.</p>

<div class="titlebanner"><h2>Community Involvement</h2></div>
